# read
Speed = float(input())

# logic
if Speed <= 10:
    print('slow')
elif 10 < Speed <= 50:
    print("average")
elif 50 < Speed <= 150:
    print("fast")
elif 150 < Speed <= 1000:
    print("ultra fast")
else:
    print("extremely fast")
